# Mathematics (Sums and algebra)

x = 10 # Set 'x' to ten
y = x * 10 # Should equal 100 (Because 10 ('x') * 10)
print ("Our value:", y)

divide = 10 / 2 # Our dividing syntax
multiply = 10 * 2 # Our multiplying syntax
toPowerOf = 10 ** 2 # Our toPowerOf syntax (10^2)
addition = 10 + 2 # Our add syntax
subtraction = 10 - 2 # Our subtract syntax

print("10 / 2 =", divide)
print("10 * 2 =", multiply)
print("10 ^ 2 =", toPowerOf)
print("10 + 2 =", addition)
print("10 - 2 =", subtraction)
